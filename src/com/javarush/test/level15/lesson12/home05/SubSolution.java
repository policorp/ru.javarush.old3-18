public class SubSolution extends Solution
{
    SubSolution()
    {
    }

    SubSolution(int a)
    {
        super(a);
    }

    SubSolution(float a)
    {
        super(a);
    }

    protected SubSolution(double a)
    {
        super(a);
    }

    protected SubSolution(short a)
    {
        super(a);
    }

    protected SubSolution(int a, int b)
    {
        super(a, b);
    }

    public SubSolution(int a, double b)
    {
        super(a, b);
    }

    public SubSolution(int a, short b)
    {
        super(a, b);
    }

    public SubSolution(short a, int b)
    {
        super(a, b);
    }

    private SubSolution (String x) {}
    private SubSolution (String x, int a) {}
    private SubSolution (long a) {}

}
